#!/usr/bin/env node
// ─────────────────────────────────────────────────────────────────────────
// BUILD PARENTHETICAL NAME FIXES  → src/data/canonical/nameFixes.generated.js
//
// Wikipedia disambiguation names like "Dida (footballer, born 1934)" arrive from
// the Wikidata import. The parenthetical is often the ONLY thing telling two
// different players apart, so we never blindly strip it. Safe rules:
//   • unique base, no clash with a real player → strip fully  ("Amarildo (…)" → "Amarildo")
//   • base clashes but a birth YEAR exists     → shorten, stay distinct ("Dida (…born 1934)" → "Dida (1934)")
//   • base clashes and NO year                 → leave as-is (can't disambiguate)
//   • ambiguous base ("Ronaldo")               → leave (handled by ambiguity, not here)
// The corrupt/original form is still kept as an alias by the identity build.
//
// Run:  node scripts/build-name-fixes.mjs
// ─────────────────────────────────────────────────────────────────────────
import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import path from 'node:path'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.join(__dirname, '..')
const OUT = path.join(ROOT, 'src/data/canonical/nameFixes.generated.js')

const norm = s => (s || '').normalize('NFD').replace(/[̀-ͯ]/g, '').toLowerCase().replace(/[^a-z0-9\s]/g, ' ').replace(/\s+/g, ' ').trim()
// Bare tokens that are genuinely ambiguous (mirrors build-identity SEED_AMBIGUOUS).
const AMBIGUOUS_BASES = new Set(['ronaldo'])

// Parenthetical SOURCE names come from Wikidata (they never change → idempotent).
const wd = JSON.parse(readFileSync(path.join(ROOT, 'src/data/canonical/wikidata.generated.json'), 'utf8'))
const names = new Set()
for (const g of ['clubs', 'nationalities', 'trophies']) for (const arr of Object.values(wd[g] || {})) for (const p of arr) names.add(p.name)
// CLASH detection uses the FULL registry (all sources), not just Wikidata — else a
// base clashing with a non-Wikidata player would be stripped and merged (e.g.
// "Xavi (footballer, born 1980)" → the famous "Xavi"). Registry is read as a
// stable reference from the previous build.
const reg = JSON.parse(readFileSync(path.join(ROOT, 'src/data/canonical/players.registry.json'), 'utf8'))
const clashSet = new Set()
for (const r of reg) { for (const n of [r.displayName, ...(r.aliases || [])]) if (!/\([^)]*\)\s*$/.test(n)) clashSet.add(n) }

const isParen = n => /\([^)]*\)\s*$/.test(n)
const baseOf = n => n.replace(/\s*\([^)]*\)\s*$/, '').trim()
const yearOf = n => (n.match(/born\s+(?:\w+\s+)?(\d{4})/) || [])[1] || null

const nonParenNorms = new Set([...clashSet].map(norm))
const parens = [...names].filter(isParen)
const baseCount = {}
for (const p of parens) { const nb = norm(baseOf(p)); baseCount[nb] = (baseCount[nb] || 0) + 1 }

const fixes = {}
let stripped = 0, shortened = 0, left = 0
for (const p of parens) {
  const base = baseOf(p), nb = norm(base), year = yearOf(p)
  if (AMBIGUOUS_BASES.has(nb)) { left++; continue }
  const clash = nonParenNorms.has(nb) || baseCount[nb] > 1
  if (!clash) { fixes[p] = base; stripped++ }
  else if (year) { fixes[p] = `${base} (${year})`; shortened++ }
  else { left++ }
}

const sorted = Object.fromEntries(Object.entries(fixes).sort((a, b) => a[0].localeCompare(b[0])))
const body = '// GENERATED by scripts/build-name-fixes.mjs — do not edit by hand.\n' +
  '// Parenthetical Wikipedia disambiguation names, cleaned where safe (never merging namesakes).\n' +
  'export default ' + JSON.stringify(sorted, null, 2) + '\n'
writeFileSync(OUT, body)
process.stderr.write(`name-fixes: ${Object.keys(sorted).length} parenthetical fixes (${stripped} stripped, ${shortened} shortened, ${left} left as-is)\n`)
